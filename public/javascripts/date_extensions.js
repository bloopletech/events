Date.prototype.toISO8601 = function() {
  function pad(n) {
    return n < 10 ? '0' + n : n;
  }
  return this.getUTCFullYear() + '-' + pad(this.getUTCMonth() + 1) + '-' + pad(this.getUTCDate()) + 'T' + pad(this.getUTCHours())
   + ':' + pad(this.getUTCMinutes()) + ':' + pad(this.getUTCSeconds()) + 'Z';
};